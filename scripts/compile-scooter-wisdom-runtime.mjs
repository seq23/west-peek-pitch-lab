#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const sourcePath = path.join(root, 'content/scooter-wisdom/approved/approved-wisdom.json');
const targetPath = path.join(root, 'src/server/ai/generatedApprovedScooterWisdom.mjs');
const registry = JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
const output = `// Generated by scripts/compile-scooter-wisdom-runtime.mjs. Do not edit by hand.\nexport const approvedWisdom = ${JSON.stringify(registry, null, 2)};\n`;
fs.writeFileSync(targetPath, output);
console.log(`Compiled approved Scooter wisdom: ${registry.version} (${registry.approvedChunks?.length || 0} chunks)`);
